s = input()
print(s[2])  # третий символ строки
print(s[-2])  # предпоследний символ
print(s[:5])  # первые пять символов
print(s[0:-2])  # вся строка кроме двух последних символов
print(s[::2])  # все символы с четными индексами в т.ч. с 0
print(s[1::2])  # все символы с нечетными индексами т.е. со второго символа
print(s[::-1])  # все символы в обратном порядке
print(s[::-2])  # все символы строки через один в обратном порядке
print(len(s))  # длина строки
